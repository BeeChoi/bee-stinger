#List Slicing
days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday']
print(days)

weekend = days[5:7]
print(weekend)

beginWeek = days[0:3]
print(beginWeek)

endWeek = days[2:5]
print(endWeek)
